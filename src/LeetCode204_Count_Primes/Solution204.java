public class Solution204 {
    public int countPrimes(int n) {
        if (n <= 2) return 0;
        //初始化数组
        boolean [] nums=  new boolean[n];
        //从2开始遍历，因为从2以后才开始出现质数
        for (int i = 2; i*i < n; i++) {
            if(!nums[i]){
                //如果没有判断过
                for (int j = 2; j *i< n; j++) {
                    //符合倍数
                    nums[i*j]=true;
                }
            }
        }
        //进行统计
        int cnt = 0;
        for (int i = 2; i != n; i++)
            if (!nums[i]) cnt++;
        return cnt;
    }
}
